var parity = function (a) {
  if (a % 2 == 0) {
    return 1;
  }
  else {
    return 0;
  }
}

const arr = [2, 5, 7, 8, 10, 14, 20, 25, 30];
var new_arr = [];
var count = arr.length;
var i = 0;
while (i < count) {
  if (parity(arr[i]) == 1) {
    new_arr.push(arr[i]);
  } i++;
}

