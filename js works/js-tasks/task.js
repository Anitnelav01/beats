const workers = [
  {"name": "John", "salary": 500},
  {"name": "Mike", "salary": 1300},
  {"name": "Linda", "salary": 1500}];


function getWorthyWorkers(workers) {
  //определяем размер массива
  var count_workers = workers.length;

  //зарплата по которой отбираем сотрудников
  const salary = 1000;

  //инициализируем начало счетчика для массива workers
  let i = 0;

  //создаем результирующий массив
  var result_arr = [];

  //запускаем цикл. цикл будет повторяться до тех пор пока элемент i не будет равен количеству элементов массива count_workers
  while (i < count_workers) {
      //создаем условие в котором будем отбирать сотрудников чья зп выше чем значение в переменной salary
      if (workers[i].salary > salary) {
          //вставляем в результирующий массив тех сотрудников чья зп выше чем зп указанная в константе salary
          result_arr.push(workers[i]);
      }
      //инкрементим i
      i++;
      
  }

  //возвращаем результирующий массив
  return result_arr;

}

//выводим в консоль результат работы функции getWorthyWorkers
console.log("Результат задачи - #2-Выборка объекта - ");
console.log(getWorthyWorkers(workers));


