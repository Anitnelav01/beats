const path = "/users/download/index.html"

function isHtml(path) {
    //определяем последение пять символов с конца строки которая содержится в path
    var line_ending = path.slice(-5);
    //сравниваем переменную line_ending со строкой .html если условие выполняется функция возвращает true если нет то false
    if (line_ending === '.html') {
        return true;
    } else {
        return false;
    }
}

